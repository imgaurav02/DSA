class Solution{
    public:
    // arr : given array
    // l : starting index of the array i.e 0
    // r : ending index of the array i.e size-1
    // k : find kth smallest element and return using this function
    int kthSmallest(int arr[], int l, int r, int k) {
        //code here
        int max = arr[0],i;
        for(i=1;i<=r;i++){
            if(arr[i]>max)
                max=arr[i];
        }

        int a[max+1]={0};
        for(i=0;i<=r;i++){
            a[arr[i]]++;
        }

        int count=0;
        for(i=0;i<max+1;i++){
            if(a[i]>0)
                count++;
            if(count==k)
                break;
        }
        return i;
    }
   
};

